/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode() {}
 *     TreeNode(int val) { this.val = val; }
 *     TreeNode(int val, TreeNode left, TreeNode right) {
 *         this.val = val;
 *         this.left = left;
 *         this.right = right;
 *     }
 * }
 */
class Solution {
    public int kthSmallest(TreeNode root, int k) {
        List<Integer> list = inorderTraverse(root, new ArrayList<Integer>());
        return list.get(k-1);
    }

    public List<Integer> inorderTraverse(TreeNode root, ArrayList<Integer> list){
        if(root == null){
            return list;
        }
        inorderTraverse(root.left, list);
        list.add(root.val);
        inorderTraverse(root.right, list);
        return list;
    }

    public int kthSmallest0(TreeNode root, int k){
        if(root == null){
            return -1;
        }
        Deque<TreeNode> stack = new ArrayDeque<TreeNode>();
        TreeNode cur = root;
        int count = 0;
        while(cur != null || !stack.isEmpty()){
            while(cur != null){
                stack.push(cur);
                cur = cur.left;
            }
            cur = stack.pop();
            count++;
            if(count == k){
                return cur.val;
            }
            cur = cur.right;
        }
        return -1;
    }
}
